package com.cma.intervideo.util;

public class SystemConfiguration {
	private String icmHost;
	private String icmPort;
	private String defaultServiceTemplateId;
	public String getIcmHost() {
		return icmHost;
	}
	public void setIcmHost(String icmHost) {
		this.icmHost = icmHost;
	}
	public String getIcmPort() {
		return icmPort;
	}
	public void setIcmPort(String icmPort) {
		this.icmPort = icmPort;
	}
	public String getDefaultServiceTemplateId() {
		return defaultServiceTemplateId;
	}
	public void setDefaultServiceTemplateId(String defaultServiceTemplateId) {
		this.defaultServiceTemplateId = defaultServiceTemplateId;
	}
	
}
